#!/usr/bin/env node
// Build a Codex marketplace root from this dual-host plugin source checkout.
//
// Codex marketplace roots must contain `.agents/plugins/marketplace.json`,
// and plugin entries are resolved from normal relative paths under that root.
// This source checkout is also a Claude plugin root, so the release package is
// materialized into dist/codex-marketplace/plugins/copilot-companion.

import {
  cpSync,
  existsSync,
  mkdirSync,
  readFileSync,
  rmSync,
  writeFileSync,
} from 'node:fs';
import { homedir } from 'node:os';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const HERE = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.dirname(HERE);
const PLUGIN_NAME = 'copilot-companion';
const DEFAULT_OUT = path.join(REPO_ROOT, 'dist', 'codex-marketplace');
const BUILD_MARKER = '.copilot-companion-codex-marketplace';

const args = process.argv.slice(2);
const outArg = readOption('--out') || DEFAULT_OUT;
const outRoot = path.resolve(REPO_ROOT, outArg);
const pluginRoot = path.join(outRoot, 'plugins', PLUGIN_NAME);
const marketplacePath = path.join(outRoot, '.agents', 'plugins', 'marketplace.json');

function readOption(name) {
  const idx = args.indexOf(name);
  if (idx === -1) return null;
  const value = args[idx + 1];
  if (!value || value.startsWith('--')) {
    fail(`${name} requires a value`);
  }
  return value;
}

function fail(message) {
  console.error(`[FAIL] ${message}`);
  process.exit(2);
}

function assertSafeOutDir(dir) {
  const root = path.parse(dir).root;
  const forbidden = new Set([
    root,
    homedir(),
    REPO_ROOT,
    path.dirname(REPO_ROOT),
  ].map((p) => path.resolve(p)));
  if (forbidden.has(path.resolve(dir))) {
    fail(`refusing to use unsafe output directory: ${dir}`);
  }
  if (existsSync(dir) && !existsSync(path.join(dir, BUILD_MARKER))) {
    fail(`refusing to replace existing directory without ${BUILD_MARKER}: ${dir}`);
  }
}

function shouldCopy(src) {
  const base = path.basename(src);
  if (base === 'node_modules' || base === '.plugin-data' || base === 'dist') return false;
  if (base === '.DS_Store' || base.endsWith('.log')) return false;
  if (base.endsWith('.test.mjs')) return false;
  return true;
}

function copyRequired(relPath) {
  const src = path.join(REPO_ROOT, relPath);
  const dest = path.join(pluginRoot, relPath);
  if (!existsSync(src)) fail(`missing required source path: ${relPath}`);
  cpSync(src, dest, {
    recursive: true,
    force: true,
    filter: shouldCopy,
  });
}

assertSafeOutDir(outRoot);
rmSync(outRoot, { recursive: true, force: true });
mkdirSync(pluginRoot, { recursive: true });
writeFileSync(path.join(outRoot, BUILD_MARKER), 'generated by scripts/build-codex-marketplace.mjs\n');

for (const relPath of [
  '.codex-plugin',
  'bridge-server',
  'hooks',
  'lib',
  'scripts',
  'templates',
  'LICENSE',
  'README.md',
]) {
  copyRequired(relPath);
}

const manifest = JSON.parse(readFileSync(
  path.join(pluginRoot, '.codex-plugin', 'plugin.json'),
  'utf8',
));
if (manifest.name !== PLUGIN_NAME) {
  fail(`plugin manifest name must be ${PLUGIN_NAME}`);
}
if (manifest.hooks !== './hooks/hooks-codex.json') {
  fail('plugin manifest must point hooks to ./hooks/hooks-codex.json');
}

mkdirSync(path.dirname(marketplacePath), { recursive: true });
writeFileSync(marketplacePath, JSON.stringify({
  name: 'copilot-companion',
  interface: {
    displayName: 'Copilot Companion',
  },
  plugins: [
    {
      name: PLUGIN_NAME,
      source: {
        source: 'local',
        path: `./plugins/${PLUGIN_NAME}`,
      },
      policy: {
        installation: 'AVAILABLE',
        authentication: 'ON_INSTALL',
      },
      category: 'Engineering',
    },
  ],
}, null, 2) + '\n');

console.log(`Codex marketplace built: ${outRoot}`);
console.log(`Plugin root: ${pluginRoot}`);
console.log(`Marketplace: ${marketplacePath}`);
